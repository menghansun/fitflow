  /// 根据运动类型获取随机激励语
  static String forWorkoutType(String type) {
    final list = switch (type) {
      'swim' => _swim,
      'cardio' => _cardio,
      'gym' => _gym,
      _ => _daily,
    };
    return list[_random.nextInt(list.length)];
  }

  /// 根据连续天数获取里程碑话语（返回 null 表示不是里程碑）
  static String? forStreak(int days) {
    // 找最接近的里程碑
    final milestones = _streak.keys.toList()..sort();
    for (final m in milestones.reversed) {
      if (days == m) {
        final list = _streak[m]!;
        return list[_random.nextInt(list.length)];
      }
    }
    return null;
  }

  /// 首页每日一句（按日期固定，不随刷新变）
  static String dailyQuote() {
    final dayOfYear = DateTime.now().difference(DateTime(DateTime.now().year)).inDays;
    return _daily[dayOfYear % _daily.length];
  }
